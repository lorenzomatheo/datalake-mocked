def process_competitor_eans(campaign_id, dados_para_inserir):
    conexao = postgres.get_connection()
    try:
        df_relations = pd.read_csv(io.StringIO(dados_para_inserir), sep="\t")
        df_relations['ean_campanha'] = df_relations['ean_campanha'].astype(str)
        df_relations['ean_concorrente'] = df_relations['ean_concorrente'].astype(str)
        print(f"Carreguei {len(df_relations)} pares de EANs")

        campaign_query = f"""
        SELECT
            cp.id AS campanhaproduto_id,
            p.ean AS ean_campanha
        FROM
            campanhas_campanhaproduto cp
        JOIN
            produtos_produtov2 p ON cp.produto_id = p.id
        WHERE
            cp.campanha_id = '{campaign_id}'
        """

        df_campaign_products = pd.DataFrame(
            postgres.execute_query(campaign_query, conexao)
        )
        print(f"Encontrei {len(df_campaign_products)} produtos na campanha informada")

        all_competitor_eans = df_relations["ean_concorrente"].unique().tolist()

        ean_placeholders = ",".join([f"'{ean}'" for ean in all_competitor_eans])
        competitor_query = f"""
        SELECT
            id AS produtov2_id,
            ean
        FROM
            produtos_produtov2
        WHERE
            ean IN ({ean_placeholders})
        """

        df_competitor_products = pd.DataFrame(
            postgres.execute_query(competitor_query, conexao)
        )
        print(f"Encontrei {len(df_competitor_products)} ids de eans concorrentes")

        ean_to_id = dict(
            zip(df_competitor_products["ean"], df_competitor_products["produtov2_id"])
        )

        insert_data = []
        for _, campaign_row in df_campaign_products.iterrows():
            campanhaproduto_id = campaign_row["campanhaproduto_id"]
            ean_campanha = campaign_row["ean_campanha"]

            matching_competitor_eans = df_relations[
                df_relations["ean_campanha"] == ean_campanha
            ]["ean_concorrente"].tolist()

            for ean_concorrente in matching_competitor_eans:
                if ean_concorrente in ean_to_id:
                    produtov2_id = ean_to_id[ean_concorrente]
                    insert_data.append((campanhaproduto_id, produtov2_id))
                else:
                    print(f"Não encontrei produto com esse EAN {ean_concorrente}")

        if not insert_data:
            print("Nada para inserir")
            return

        chunk_size = 500
        chunks = [
            insert_data[i : i + chunk_size]
            for i in range(0, len(insert_data), chunk_size)
        ]

        total_inserted = 0
        for chunk in chunks:
            values_str = ",".join([f"('{cp_id}', '{p_id}')" for cp_id, p_id in chunk])

            insert_query = f"""
            INSERT INTO campanhas_campanhaproduto_produtos_concorrentes
            (campanhaproduto_id, produtov2_id)
            VALUES {values_str}
            ON CONFLICT DO NOTHING
            """

            postgres.execute_query(insert_query, conexao).display()
            total_inserted += len(chunk)

        print(
            f"{total_inserted} pares de ean campanha e ean concorrentes inseridos com sucesso!"
        )
    finally:
        conexao.close()


